# Classe Pessoa que será usada para herança das outras classes
class Pessoa:

    # método para setar o nome:
    def set_nome(self, nome):
        self.nome = nome
    
    # método para retornar nome
    def get_nome(self):
        return self.nome

    # método para setar o cpf:
    def set_cpf(self, cpf):
        self.cpf = cpf

    # método para retornar o cpf
    def get_cpf(self):
        return self.cpf

    # método para setar a idade:
    def set_idade(self, idade):
        self.idade = idade
        

    # método para retornar idade
    def get_idade(self):
        return self.idade




